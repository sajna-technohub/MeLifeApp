package com.technohub.melifeapp.models;

public class AnswerOptions {
    private String option_id;
    private String option_no;
    private String option_descp;
    private String pop_up_id;

    public String getOption_id() {
        return option_id;
    }

    public void setOption_id(String option_id) {
        this.option_id = option_id;
    }

    public String getOption_no() {
        return option_no;
    }

    public void setOption_no(String option_no) {
        this.option_no = option_no;
    }

    public String getOption_descp() {
        return option_descp;
    }

    public void setOption_descp(String option_descp) {
        this.option_descp = option_descp;
    }

    public String getPop_up_id() {
        return pop_up_id;
    }

    public void setPop_up_id(String pop_up_id) {
        this.pop_up_id = pop_up_id;
    }
}
